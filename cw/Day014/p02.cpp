/**
 * This C++ program demonstrates the use of left shift and right shift operators.
 */
// WAP for left shift and right shift.

#include<iostream>
using namespace std;
int main()
{

    int a = 12;
    cout << a << endl;
    int b = a<< 1;
    cout << b << endl;

    int m = 8;
    cout << m << endl;
    int n = m>> 1;
    cout << n << endl;
    
}