/**
 * This program tests the bitwise operators in C++.
 */
// WAP to test bitwise operators.

#include<iostream>
using namespace std;
int main()
{

    bool a = false;
    bool b = true;

    cout << (a&b)<< endl;
    cout << (a|b) << endl;
    cout << (~a) << endl;
    cout << (a^b) << endl;
    
}