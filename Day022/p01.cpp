/**
 * This C++ program initializes an array with 5 integers and then prints each value on a new line.
 */


#include<iostream>
using namespace std;
int main()
{

    int arr[5] = {9,8,7,6,5};

    // printing all values 

    for (int i = 0; i < 5; i++)
    {
        cout << arr[i] << endl;
    }
    

}